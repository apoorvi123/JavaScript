// logical and (&&)
console.log(false && false);
console.log(false && true);
console.log(true && false);
console.log(true && true);

console.log(4>2 && 0<1);
console.log(4>=2 && 0<1);
console.log("4>2" && 0<1);

// logical or (||)
console.log(false || false);
console.log(false || true);
console.log(true || false);
console.log(true || true);

console.log(4>2 || 0<1);
console.log(4>=2 || 0<1);
console.log("4>2" || 0<1);

// logical not (!)
console.log(!true);
console.log(!false);
